		OR	TEMP,AL			; SET CHARACTER VALUE
		JZ	SHORT .D86A_L1		; DONE

; scan = 0; putd(ALT_INPUT,0,0);

		XOR	AX,AX
		MOV	SCAN_CHAR,AL
		MOV	ALT_INPUT,AL		; 0
		JMP	.D7BA			; SAVE VALUE

		PAGE

